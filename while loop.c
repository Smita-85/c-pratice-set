#include<stdio.h>
//printing 'HELLO WORLD'100 times using while loop..!!@@
int main(){
    int i=0;
    while(i<=100)
    {
        if(i==100)
        {
            printf("HELLO WORLD_100");
        }
        else
        {
             printf("HELLO WORLD\n");
        }
        i++;
        for(int i=1;i<=5;i++)
        {
        printf("%d\n",i);
        
        }
    }
        

    
    return 0;
}